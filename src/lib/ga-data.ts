function parseRow(
  dateHour: string,
  sessions: number,
  engagedSessions: number,
  engagementRate: number,
  avgEngagementTime: number,
  eventsPerSession: number,
  formSubmits: number,
): GAHourlyRecord {
  const year = dateHour.slice(0, 4);
  const month = dateHour.slice(4, 6);
  const day = dateHour.slice(6, 8);
  const hour = parseInt(dateHour.slice(8, 10), 10);
  return {
    dateHour,
    date: `${year}-${month}-${day}`,
    hour,
    sessions,
    engagedSessions,
    engagementRate,
    avgEngagementTime,
    eventsPerSession,
    formSubmits,
  };
}

